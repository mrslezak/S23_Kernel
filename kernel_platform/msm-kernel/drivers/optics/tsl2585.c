static ssize_t tsl2585_regs_write_store (struct device *dev,
		struct device_attribute *attr,
		const char *buf, size_t size)
{
	int num = 0;
	int ret = 0;
	u8 reg = 0x00;

	char r_value = 0x00;
	char w_value = 0x00;

	struct tsl2585_device_data *data = dev_get_drvdata(dev);

	num = sscanf(buf,"W:0x%hhx,0x%hhx",&reg,&w_value);

	mutex_lock(&data->activelock);

	ret = tsl2585_read_reg(data, (u8)reg, &r_value, 1);
	if (ret != 0) {
		ALS_err("%s - err=%d, val=0x%06x\n",
				__func__, ret, r_value);
		mutex_unlock(&data->activelock);
		return ret;
	}

	ret = tsl2585_pon_reenable(data,true);//pon

	if (ret < 0) {
		ALS_err("%s - failed to AMS_ENABLE_PON\n", __func__);
		mutex_unlock(&data->activelock);
		return 0;
	}

	ALS_dbg("%s   read reg 0x%x , val 0x%x \n",__func__, reg,r_value);

	ret = tsl2585_write_reg(data, (u8)reg, (u8)w_value);
	if (ret < 0) {
		ALS_err("%s - fail err = %d\n", __func__, ret);
		mutex_unlock(&data->activelock);
		return ret;
	}

	ALS_dbg("%s   write reg 0x%x , val 0x%x \n",__func__, reg,w_value);

	ret = tsl2585_pon_reenable(data,false);//reenable

	if (ret < 0) {
		ALS_err("%s - failed to AMS_REENABLE\n", __func__);
		mutex_unlock(&data->activelock);
		return 0;
	}

	mutex_unlock(&data->activelock);
	return size;
}

