static uint32_t calc_average(uint32_t *buffer, int count)
{
	uint32_t sum = 0;
	int i;
	uint16_t average = 0;

	if ((NULL != buffer) && (count > 0))
	{
		for (i = 0; i < count; i++)
		{
			sum += buffer[i];
		}
		average = sum / count;
	}
	return average;
}

#define TSL2585_THD_CLEAR      1800LL    /* 1.8 * 1000 */
#define TSL2585_THD_RATIO      3LL       /* 0.003 * 1000 */
#define TSL2585_GAIN_MAX       4096LL    /* 4096 */
#define TSL2585_THD_RATIO_AUTO 1000LL       /*  */
//#define MAX_NUM_FLICKER_SAMPLES 2048LL
#define MAX_NUM_FLICKER_SAMPLES AMS_FFT_SIZE
#define MAX_NUM_FLICKER_SAMPLE_BYTES (MAX_NUM_FLICKER_SAMPLES * 4)

static uint64_t calc_thd(uint16_t clear_avg_fifo, uint32_t clear_avg, uint32_t *fft_out_data)
{
	uint64_t threshold;
	uint64_t ratio_fixed;
	uint64_t ratio_final;

	if (clear_avg_fifo <= 1) {
		ratio_fixed = (TSL2585_THD_CLEAR * TSL2585_GAIN_MAX);
	} else {
		if (clear_avg) {
			ratio_fixed = (TSL2585_THD_CLEAR << 7)/ clear_avg; // clear_avg left-shifted 7.
		}
		else {
			ratio_fixed = (TSL2585_THD_CLEAR << 7); // clear_avg left-shifted 7.
		}
	}

	if ((TSL2585_THD_RATIO_AUTO) > ratio_fixed) {
		ratio_final = TSL2585_THD_RATIO_AUTO;
	} else {
		ratio_final = ratio_fixed;
	}

	threshold = ((uint64_t)fft_out_data[0] * ratio_final * TSL2585_THD_RATIO)/1000000;

	ALS_info("calc_thd threshold = %lld, fifoout[0] %u, ratio_final %lld \n", threshold, fft_out_data[0], ratio_final);
	return threshold;
}

static uint8_t fifo_out_data[AMS_FLICKER_NUM_SAMPLES];

static void parse_fifo_end_data(ams_deviceCtx_t * ctx )
{
	uint8_t fifo_end_data[7];
	int fifo_end_size = 0;
	int fifo_end_len;
	int gain_index = 0;
	int checksum_index = 0;
	uint16_t tmp;
	uint8_t kfifolen = 0;

	/* Calculate the number of end bytes */
	if (true == ctx ->has_fifo_fd_end_marker)
	{
		fifo_end_size += 3;
	}
	if (true == ctx ->has_fifo_fd_checksum)
	{
		fifo_end_size += 2;
	}
	if (true == ctx ->has_fifo_fd_gain)
	{
		fifo_end_size += 2;
	}

	/* Get the end bytes */
	memset(fifo_end_data, 0x0, sizeof(fifo_end_data));
	if ((kfifolen = kfifo_len(&ams_fifo)) >= fifo_end_size)
	{
		fifo_end_len = kfifo_out(&ams_fifo, fifo_end_data, fifo_end_size);
	}

	/* Determine the index for the various end data */
	if ((true == ctx ->has_fifo_fd_end_marker) &&
			(true == ctx ->has_fifo_fd_checksum) &&
			(true == ctx ->has_fifo_fd_gain))
	{
		checksum_index = 3;
		gain_index = 5;
	}
	else if ((true == ctx ->has_fifo_fd_end_marker) &&
			(false == ctx ->has_fifo_fd_checksum) &&
			(true == ctx ->has_fifo_fd_gain))
	{
		checksum_index = 0;
		gain_index = 3;
	}
	else if ((true == ctx ->has_fifo_fd_end_marker) &&
			(true == ctx ->has_fifo_fd_checksum) &&
			(false == ctx ->has_fifo_fd_gain))
	{
		checksum_index = 3;
		gain_index = 0;
	}
	else if ((false == ctx ->has_fifo_fd_end_marker) &&
			(true == ctx ->has_fifo_fd_checksum) &&
			(true == ctx ->has_fifo_fd_gain))
	{
		checksum_index = 0;
		gain_index = 2;
	}
	else if ((false == ctx ->has_fifo_fd_end_marker) &&
			(false == ctx ->has_fifo_fd_checksum) &&
			(true == ctx ->has_fifo_fd_gain))
	{
		checksum_index = 0;
		gain_index = 0;
	}
	else if ((false == ctx ->has_fifo_fd_end_marker) &&
			(true == ctx ->has_fifo_fd_checksum) &&
			(false == ctx ->has_fifo_fd_gain))
	{
		checksum_index = 0;
		gain_index = 0;
	}


	/* Parse the data */
	/* Data is written to the fifo in little endian, byte swap is required */
	if (true == ctx->has_fifo_fd_checksum)
	{
		tmp = ((fifo_end_data[checksum_index + 1] << 8) | fifo_end_data[checksum_index]);
		ctx->fifo_checksum = tmp;
	}

	if ((true == ctx->has_fifo_fd_gain) && ((fifo_end_data[0] == 0) && (fifo_end_data[1] == 0) && (fifo_end_data[2] == 0)) && (kfifolen >= fifo_end_size))
	{
		tmp = ((fifo_end_data[gain_index + 1] << 8) | fifo_end_data[gain_index]);

		ALS_info("%s: tmp: %04x ,  fifo_end_data[%d] =0x%x , fifo_end_data[+1] = 0x%x , \n", __func__, tmp,gain_index,fifo_end_data[gain_index],fifo_end_data[gain_index+1]);
		/* After the byte swap the gain data is like so
		 *
		 *                       Bits
		 *      15-12       11-8        7-4          3-0
		 *  -------------------------------------------------
		 * |  not used | mod2 gain | mod1 gain  | mod0 gain  |
		 *  -------------------------------------------------
		 *
		 */
		ctx->fifo_mod0_gain = tmp & 0xFF;
		ctx->fifo_mod2_gain = (tmp >> 8) & 0x0F;
	}

}

ssize_t read_fifo(ams_deviceCtx_t * ctx , uint16_t *buf, int size)
{
	int len;
	int kfifo_Len;
	int i =0,j =0 ;
	uint16_t tmp =0;

	memset(fifo_out_data, 0x0, sizeof(fifo_out_data));

	kfifo_Len =  kfifo_len(&ams_fifo);

	if(kfifo_Len >=size){ //512
		len = kfifo_out(&ams_fifo, fifo_out_data, size);
		/* byte swap flicker data*/
		for (i = 0, j = 0; i < size; i+=2, j++) {
			tmp = ((fifo_out_data[i + 1] << 8) | fifo_out_data[i]); //256
			buf[j] = tmp;
		}

		parse_fifo_end_data(ctx);
	} else {
		len = 0;
	}
	ALS_info("read_fifo read size  %d , kfifo_Len =%d\n",len,kfifo_Len);
	return len;
}

#define CHANNEL 3 //(Photopic, IR, UVA)

static uint16_t buffer[AMS_FLICKER_NUM_SAMPLES] ;
int get_fft(ams_deviceCtx_t * ctx , uint32_t *out)
{
	static uint32_t clear_buffer[AMS_FFT_SIZE] = { 0 };
	static uint32_t wideband_buffer[AMS_FFT_SIZE] = { 0 };
	static uint32_t uv_buffer[AMS_FFT_SIZE] = { 0 };
	ssize_t size = 0;
	int i, j = 0;
	uint16_t clear_gain, wideband_gain = 0;
	uint8_t fifo_mod0_gain, fifo_mod1_gain = 0;
	uint16_t uv_gain;
	uint8_t fifo_mod2_gain=0;
	int ret = 0;

	memset(clear_buffer, 0, AMS_FFT_SIZE * sizeof(uint32_t));
	memset(wideband_buffer, 0, AMS_FFT_SIZE * sizeof(uint32_t));
	memset(uv_buffer, 0, AMS_FFT_SIZE * sizeof(uint32_t));
	memset(buffer, 0x00, AMS_FLICKER_NUM_SAMPLES * 2);

	size = read_fifo(ctx,buffer,AMS_FLICKER_NUM_SAMPLES); //512 byte
	if (size > AMS_FLICKER_NUM_SAMPLES) {
		ALS_err("%s - invalid read_fifo size = %d", __func__, size);
		size = AMS_FLICKER_NUM_SAMPLES;
	};

	kfifo_reset(&ams_fifo);

	/* Separate Clear and Wideband data */
	for (i = 0, j = 0; i < size/BYTE; i+=CHANNEL, j++) { //768byte = 384 sample (Photopic, IR, UVA)
		/* Photopic */
		out[j] = clear_buffer[j] = buffer[i]; //separate 128
		/* IR */
		wideband_buffer[j] = buffer[i + 1]; //separate 128
		/* UVA */
		uv_buffer[j] = buffer[i + 2]; //separate 128
	}

	ctx->clear_average_fifo = calc_average(clear_buffer, AMS_CLR_WIDE_FLICKER_NUM_SAMPLES);//128
	ctx->wideband_average_fifo = calc_average(wideband_buffer, AMS_CLR_WIDE_FLICKER_NUM_SAMPLES);//128
	ctx->uv_average_fifo = calc_average(uv_buffer, AMS_CLR_WIDE_FLICKER_NUM_SAMPLES);//128

	fifo_mod0_gain = ctx->fifo_mod0_gain & 0x0F;//clear
	fifo_mod1_gain = (ctx->fifo_mod0_gain >> 4) & 0x0F;//wide
	fifo_mod2_gain = ctx->fifo_mod2_gain & 0x0F;//uv

	if (fifo_mod0_gain >= (sizeof(tsl2585_gain_conversion) / sizeof(uint16_t))) {
		ALS_err("%s - invalid gain0 = %d (fifo_mod0_gain = 0x%x)", __func__, fifo_mod0_gain, ctx->fifo_mod0_gain);
		fifo_mod0_gain = 1;
	};

	if (fifo_mod1_gain >= (sizeof(tsl2585_gain_conversion) / sizeof(uint16_t))) {
		ALS_err("%s - invalid gain1 = %d (fifo_mod1_gain = 0x%x)", __func__, fifo_mod1_gain, ctx->fifo_mod0_gain);
		fifo_mod1_gain = 1;
	};

	if (fifo_mod2_gain >= (sizeof(tsl2585_gain_conversion) / sizeof(uint16_t))) {
		ALS_err("%s - invalid gain2 = %d (fifo_mod2_gain = 0x%x)", __func__, fifo_mod1_gain, ctx->fifo_mod2_gain);
		fifo_mod2_gain = 1;
	};

	clear_gain = tsl2585_gain_conversion[fifo_mod0_gain];
	wideband_gain = tsl2585_gain_conversion[fifo_mod1_gain];
	uv_gain = tsl2585_gain_conversion[fifo_mod2_gain];

	ctx->clear_average = ((uint32_t)ctx->clear_average_fifo) << 7;
	ctx->wideband_average = ((uint32_t)ctx->wideband_average_fifo) << 7;
	ctx->clear_average /= clear_gain;
	ctx->wideband_average /= wideband_gain;
	ctx->uv_average = ((uint32_t)ctx->uv_average_fifo) << 7;
	ctx->uv_average <<= 4;
	ctx->uv_average /= uv_gain;

	ALS_info("%s - cavg_fifo: %d, wavg_fifo: %d, uvavg_fifo: %d, cavg: %d, wavg: %d uv: %d", __func__,
			ctx->clear_average_fifo, ctx->wideband_average_fifo, ctx->uv_average_fifo,
			ctx->clear_average, ctx->wideband_average);
	ALS_info("%s - gain0 = %d , gain1 = %d , gain2 = %d", __func__,
			fifo_mod0_gain, fifo_mod1_gain, fifo_mod2_gain);

	/* dev_info(&chip->client->dev, "calling ams_rfft with %d samples\n", num_samples); */

	FFT(out, AMS_FFT_SIZE);
	AMS_FIFO_CLEAR(ret);

	if (ret < 0) {
		ALS_err("%s - failed to FIFO CLEAR\n", __func__);
		return ret;
	}
	//ams_rfft(buffer, AMS_FFT_SIZE);
	//ams_get_magnitude((int16_t*)buffer,(uint16_t*)out,AMS_FFT_SIZE/2);
	return 1;
}

