static void report_als(struct tsl2585_device_data *chip)
{
	ams_apiAls_t outData = {0};
	static unsigned int als_cnt;

	if (chip->als_input_dev) {
		ams_deviceGetAls(chip->deviceCtx, &outData);

		input_report_rel(chip->als_input_dev, REL_X, outData.wideband + 1);
		input_report_rel(chip->als_input_dev, REL_RY, outData.clear + 1);
		input_report_rel(chip->als_input_dev, REL_RX, outData.rawUvCorrected + 1);

		input_report_abs(chip->als_input_dev, ABS_X, outData.time_us + 1);
		input_report_abs(chip->als_input_dev, ABS_Y, outData.ClearGain + 1);
		input_report_abs(chip->als_input_dev, ABS_Z, outData.WBGain + 1);
		input_report_abs(chip->als_input_dev, ABS_RX, outData.UVGain + 1);
		input_sync(chip->als_input_dev);

		if (als_cnt++ > 10) {
			ALS_dbg("%s - Photopic:%d, IR:%d, UV:%d(%d), P GAIN:%d, IR GAIN:%d, UVGAIN:%d\n", __func__,
					outData.clear, outData.wideband, outData.rawUv, outData.rawUvCorrected,
					outData.ClearGain, outData.WBGain, outData.UVGain);
			als_cnt = 0;
		} else {
			ALS_info("%s - Photopic:%d, IR:%d, UV:%d(%d), P GAIN:%d, IR GAIN:%d, UVGAIN:%d\n", __func__,
					outData.clear, outData.wideband, outData.rawUv, outData.rawUvCorrected,
					outData.ClearGain, outData.WBGain, outData.UVGain);
		}

#if IS_ENABLED(CONFIG_SENSORS_FLICKER_SELF_TEST)
		als_eol_update_als(outData.ir, outData.clear, outData.wideband, outData.wideband);
#endif
	}
	chip->saturation = false;
}

static void report_flicker(struct tsl2585_device_data *chip)
{
	ams_apiAlsFlicker_t outData;
	uint flicker = 0;
	static unsigned int flicker_cnt;

	if (chip->als_input_dev) {
		ams_deviceGetFlicker(chip->deviceCtx, &outData);
		flicker = outData.mHz;
		input_report_rel(chip->als_input_dev, REL_RZ, flicker + 1);
		input_sync(chip->als_input_dev);

		if (flicker_cnt++ > 10) {
			ALS_dbg("%s - flicker = %d\n", __func__, flicker);
			flicker_cnt = 0;
		} else {
			ALS_info("%s - flicker = %d\n", __func__, flicker);
		}

		chip->user_flicker_data = flicker;
#if IS_ENABLED(CONFIG_SENSORS_FLICKER_SELF_TEST)
		als_eol_update_flicker(flicker);
#endif
	}
}

