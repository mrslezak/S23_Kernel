static uint64_t ams_isqrt(uint64_t x)
{
	register uint64_t result, tmp;

	result = 0;
	tmp = (1LL << 62);  // second-to-top bit set
	while (tmp > x) {
		tmp >>= 2;
	}
	while (tmp != 0) {
		if (x >= (result + tmp)) {
			x -= result + tmp;
			result += 2 * tmp;  // <-- faster than 2 * one
		}
		result >>= 1;
		tmp >>= 2;
	}
	return result;
}

void ams_get_magnitude(int64_t* data_r, int64_t* data_i, int32_t* buffer, int size)
	// data : 30 bits
{   //data must be twice as long as size
	int i;

	for (i = 0; i < size; ++i) {
		//sqrt(real^2 + imaginary^2)
		uint64_t square = 0;
		int64_t t_r = data_r[i];
		int64_t t_i = data_i[i];

		square = ((t_r * t_r) >> 3) + ((t_i * t_i) >> 3); // 58bit
		buffer[i] = (int32_t)ams_isqrt(square); //29bit
	}
}

// n is a power of 2
int _log2n(int n)
{
	//int len = sizeof(int) * 8;
	int len = 32, i;
	for (i = 0; i < len; i++)
	{
		if ((n&1) == 1)
			return i;
		else
			n >>= 1;
	}
	return -1;
}

// Utility function for reversing the bits
// of given index x
unsigned int bitReverse(unsigned int x, int log2n)
{
	int n = 0, i;
	for (i = 0; i < log2n; i++)
	{
		n <<= 1;
		n |= (x & 1);
		x >>= 1;
	}
	return n;
}

void _fft(int32_t* a_r, int32_t* a_i, int64_t* A_r, int64_t* A_i, int n)
{
	int log2n = _log2n(n), s, k, j;
	unsigned int i;
	// bit reversal of the given array
	for (i = 0; i < n; ++i) {
		int rev = bitReverse(i, log2n);
		A_r[i] = ((int64_t)a_r[rev]) << 7; // 30+7=37 (38 with sign)
		A_i[i] = ((int64_t)a_i[rev]) << 7; // 30+7=37 (38 with sign)
	}
	for (s = 1; s <= log2n; s++) {
		int m = 1 << s;
		int m_2 = m >> 1;
		int64_t wm_r = (int64_t)cos[1024 >> s];    //wm_r = cos(2 pi / m)    Q16 (17 with sign)
		int64_t wm_i = -(int64_t)sin[1024 >> s];   //wm_i = -sin(2 pi / m)    Q16

		for (k = 0; k < n; k += m)
		{
			int64_t w_r = 1LL << 16;  // Q16 => 65536
			int64_t w_i = 0;          // Q16

			for (j = 0; j < m_2; j++)
			{
				int i1 = k + j;
				int i2 = i1 + m_2;
				int64_t t_r = w_r * A_r[i2] - w_i * A_i[i2];   // 37+16+0~10=53~63 (64 with sign)
				int64_t t_i = w_r * A_i[i2] + w_i * A_r[i2];   // 37+16+0~10=53~63 (64 with sign)
				int64_t u_r = A_r[i1];    // 37~47
				int64_t u_i = A_i[i1];    // 37~47
				int64_t w2_r = w_r * wm_r - w_i * wm_i;   // Q16*Q16,  16+16=32
				int64_t w2_i = w_r * wm_i + w_i * wm_r;   // Q16*Q16
				t_r >>= 16;    // Q16
				t_i >>= 16;    // Q16
				A_r[i1] = u_r + t_r;      // 37+16+0~10 = 53~63
				A_i[i1] = u_i + t_i;
				A_r[i2] = u_r - t_r;
				A_i[i2] = u_i - t_i;
				w_r = w2_r >> 16;    // Q16
				w_i = w2_i >> 16;    // Q16
			}
		}
	}
}

int32_t buf_r[AMS_FFT_SIZE] = {0};
int32_t buf_i[AMS_FFT_SIZE] = {0};
int64_t out_r[AMS_FFT_SIZE] = {0};
int64_t out_i[AMS_FFT_SIZE] = {0};

void FFT(int32_t* data, enum fft_size size)
	// buf_r <= 16bits data
{
	static int log_cnt = 0;
	int i, hamming_step = 2048/size;

	if (size > AMS_FFT_SIZE || (size & 0x7f) != 0) { //FFT size : multiply of 128
		//@TODO add return codes so we know it failed
		return;
	}

	for (i = 0; i < size; i++) {
#ifndef USE_GET_FIFO_ALS
		if (!tsl2585_data->saturation && data[i] >= 0x3FFF) {
			ALS_info("DEBUG_FLICKER saturation");
			tsl2585_data->saturation = true;
		}
#endif
		buf_r[i] = ((int64_t)data[i]*(int64_t)hamming[i * hamming_step]) >> 2;        // 16+16-2=30
		if (log_cnt > 30)
			ALS_info("DEBUG_FLICKER data[%d] => %d buf[%d] => %d", i, data[i], i, buf_r[i]);
	}
	_fft(buf_r, buf_i, out_r, out_i, size);	         //output 47

	for (i = 0; i < AMS_FFT_SIZE; i++) {
		out_r[i] >>= 17;
		out_i[i] >>= 17;
	}
	ams_get_magnitude(out_r, out_i, data, size);
	if (log_cnt > 30)
		log_cnt = 0;
	else
		log_cnt++;
}

